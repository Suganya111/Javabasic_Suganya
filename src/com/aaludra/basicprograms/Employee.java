package com.aaludra.basicprograms;

public class Employee {
	int id;
	String name;
	String city;
	String designation;
	double salary;
	int yoj;

	public Employee(int id, String name, String city, String designation, double salary, int yoj) {
		super();
		this.id = id;
		this.name = name;
		this.city = city;
		this.designation = designation;
		this.salary = salary;
		this.yoj = yoj;
	}

}
